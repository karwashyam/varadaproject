package com.webapp.dto;


public class DripMarkettingDto {
	
	private String dripMarketingId;
	private String marketingText;
	private long inDays;
	public String getDripMarketingId() {
		return dripMarketingId;
	}
	public void setDripMarketingId(String dripMarketingId) {
		this.dripMarketingId = dripMarketingId;
	}
	public String getMarketingText() {
		return marketingText;
	}
	public void setMarketingText(String marketingText) {
		this.marketingText = marketingText;
	}
	public long getInDays() {
		return inDays;
	}
	public void setInDays(long inDays) {
		this.inDays = inDays;
	}
	
	
		
	
}
