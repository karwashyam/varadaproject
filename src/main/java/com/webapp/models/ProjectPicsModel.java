package com.webapp.models;

public class ProjectPicsModel {

	
	private String projectPicId;
	private String projectId;
	private String projectPic;
	private String projectPicThumbnail;
	
	
	public String getProjectPicId() {
		return projectPicId;
	}
	public void setProjectPicId(String projectPicId) {
		this.projectPicId = projectPicId;
	}
	public String getProjectId() {
		return projectId;
	}
	public void setProjectId(String projectId) {
		this.projectId = projectId;
	}
	public String getProjectPic() {
		return projectPic;
	}
	public void setProjectPic(String projectPic) {
		this.projectPic = projectPic;
	}
	public String getProjectPicThumbnail() {
		return projectPicThumbnail;
	}
	public void setProjectPicThumbnail(String projectPicThumbnail) {
		this.projectPicThumbnail = projectPicThumbnail;
	}
	
	
}
